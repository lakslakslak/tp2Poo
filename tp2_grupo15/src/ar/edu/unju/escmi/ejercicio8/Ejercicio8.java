package ar.edu.unju.escmi.ejercicio8;
public class Ejercicio8 {
public static void main(String[] args) {
int a = 5, b = 10, c = 2, d = 6;
// Mostrar los valores iniciales
System.out.println("Valores iniciales:");
System.out.println("a = " + a);
System.out.println("b = " + b);
System.out.println("c = " + c);
System.out.println("d = " + d);
// Aplicar los operadores de asignación
a += 5;
b -= 2;
c *= 3;
d /= 2;
//Mostrar los valores despues de las operaciones
System.out.println("\nValores después de aplicar los operadores de asignacion:");
System.out.println("a += 5 -> a = " + a);
System.out.println("b -= 2 -> b = " + b);
System.out.println("c *= 3 -> c = " + c);
System.out.println("d /= 2 -> d = " + d);
}
}