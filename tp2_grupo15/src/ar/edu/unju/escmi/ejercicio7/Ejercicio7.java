package ar.edu.unju.escmi.ejercicio7;
public class Ejercicio7 {
public static void main(String[] args) {

int a = 30;
double b = 1.5;
System.out.println(a++); // Imprime 30, luego a es 31
System.out.println(a); // Imprime 31
System.out.println(--a); // Decrementa primero, luego imprime 30
System.out.println(++b); // Incrementa primero, luego imprime 2.5
System.out.println(b--); // Imprime 2.5, luego b es 1.5
System.out.println(b); // Imprime 1.5
}
}